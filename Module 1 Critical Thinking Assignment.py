#Part 1: Write a Python program to find the addition and subtraction of two numbers
def main():
    num1 = int(input('Enter a number: '))
    num2 = int(input('Enter another number: '))
    print('The sum of the first and second numbers is', num1 + num2)
    print('The difference between the first and second numbers is', num1 - num2)
if __name__ == '__main__': main()

